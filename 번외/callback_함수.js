/*
* 익명 콜백 함수
* 콜백 함수는 익명 함수로 사용하는 경우가 많습ㄴ디ㅏ.
* 매개변수에 익명함수를 곧바로 입력할 수도 있음
* */

//함수를 선언합니다.
function callTenTimes(callback){
    //10회 반복합니다.
    for (var i=0; i<10; i++){
        //매개변수로 전달된 함수를 호출합니다.
        callback();
    }
}
//함수를 호출합니다. 함수를 호출하는 동시에 function함수를 만들면서 넣어줄 수 있음
callTenTimes(function(){
    alert('함수 호출');
});

